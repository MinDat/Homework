def get_date(post):
    return post["date"]

def home_page(request):
    sorted_post=sorted( all_posts, key=get_date)
    latest_post=sorted_post[-3:]

    content = render(request, "blog/home_page.html", {
        "posts":latest_post
    })
    return HttpResponse(content)

def post_list(request):
    content = render(request, "blog/post_list.html", {
        "posts":all_posts
    })
    return HttpResponse(content)

def post_detail(request, slug):
    try:
        identify_post = next(post for post in all_posts if post["slug"] == slug)
        content = render(request, "blog/post_detail.html", {
            "post":identify_post,
        })
        return HttpResponse(content)
    except:
        content=render_to_string("404.html")
        return HttpResponseNotFound(content)